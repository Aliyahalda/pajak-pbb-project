	<h2>Objek Pajak yang Bebas Pajak PBB :</h2>

	<ol>
		<li>Digunakan oleh pemerintah pusat dan daerah untuk menyelenggarakan pemerintahan</li>
		<li>Digunakan semata-mata untuk melayani kepentingan umum dan tidak untuk mencari keuntungan, antara
			lain di bidang ibadah, sosial, kesehatan, pendidikan dan kebudayaan nasional</li>
		<li> Digunakan untuk kuburan, peninggalan purbakala, hutan wisata, atau yang sejenis dengan itu</li>
		<li>Merupakan hutan lindung, hutan suaka alam, hutan wisata, taman nasional, tanah negara yang belum
			dibebani suatu hak</li>
		<li>Digunakan oleh perwakilan diplomatik dan konsulat berdasarkan asas perlakuan timbal balik</li>
		<li>Digunakan oleh badan, atau perwakilan lembaga internasional yang ditetapkan dengan Peraturan Menteri
			Keuangan (PMK).</li>

	</ol>
	<h1>Dasar Pengenaan atas Pungutan Pajak Bumi & Bangunan (PBB)</h1>
	<div class="stabilo njop">
		<h2>1. Nilai Jual Objek Pajak (NJOP)</h2>
	</div>
	<p>Nilai Jual Objek Pajak (NJOP) merupakan besarnya harga atas objek baik bumi maupun bangunan atau dapat
		dikatakan pula sebagai harga untuk properti tanah dan bangunan. Sebelum menghitung berapa besarnya Pajak
		Bumi dan Bangunan (PBB) yang harus dibayarkan, maka langkah pertama harus mengetahui terlebih dulu harga
		dari tanah dan bangunan tersebut.
		<br><br>
		<strong>NJOP = (NJOP Bumi = luas tanah x nilai tanah) + (NJOP Bangunan = luas bangunan x nilai
			bangunan).</strong>
	</p>
	<div class="stabilo njoptkp">
		<h2>2. NJOPTKP</h2>
	</div>
	<p>NJOPTKP merupakan batas Nilai Jual Objek Pajak atas bumi dan bangunan yang tidak dikenai pajak. Besarnya
		NJOPTKP di masing-masing daerah berbeda-beda. Dalam penggunaan NJOPTKP memperhatikan ketentuan sebagai
		berikut :
		<br>
	<ol>
		<li>Setiap wajib pajak memperoleh pengurangan NJOPTKP sebanyak satu kali dalam 1 Tahun Pajak.</li>
		<li>Jika wajib pajak memiliki lebih dari 1 objek pajak, maka yang bisa atau mendapat pengurangan NJOPTKP
			hanya 1 objek pajak yang nilainya paling besar dan tidak bisa digabungkan dengan objek pajak lainnya
			yang wajib pajak miliki.</li>
	</ol>
	</p>
	<div class="stabilo njkp">
		<h2>3. Nilai Jual Kena Pajak (NJKP)</h2>
	</div>
	<p>Nilai Jual Kena Pajak (NJKP) merupakan suatu dasar dari penghitungan Pajak Bumi dan Bangunan (PBB)
		sebagai nilai jual objek yang akan dimasukkan ke dalam perhitungan pajak yang terutang. Berikut ini
		merupakan ketentuan persentase dari Nilai Jual Kena Pajak (NJKP) yang telah ditetapkan pemerintah
		berdasarkan Keputusan Menteri Keuangan No.201/KMK.04/2000 Tentang Penyesuaian Besarnya Nilai Jual Objek
		Pajak Tidak Kena Pajak Sebagai Dasar Penghitungan Pajak Bumi dan Bangunan :
		<br>
	<ol>
		<li>40% (empat puluh persen) untuk perkebunan</li>
		<li>40% (empat puluh persen) untuk pertambangan</li>
		<li>40% (empat puluh persen) untuk kehutanan</li>
	</ol>
	<br>
	Sedangkan bagi objek pajak lainnya seperti pedesaan dan perkotaan dapat dilihat dari Nilai Jual Objek Pajak
	(NJOP), yaitu: 40% (empat puluh persen) untuk nilai lebih dari Rp 1.000.000.000 (satu miliar rupiah),
	sedangkan 20% (dua puluh persen) untuk nilai kurang dari Rp 1.000.000.000 (satu miliar rupiah).
	</p>

