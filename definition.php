<div class="definition">
	<h1>Pajak</h1>
	<p>Pajak adalah pungutan yang wajib diberikan pada negara oleh orang pribadi maupun badan/perusahaan
		berdasarkan undang-undang yang akan digunakan untuk kepentingan negara dan kesejahteraan masyarakat
		umum. Pemungutan, pelayanan, dan pengawasan pajak dilakukan oleh Direktorat Jenderal Pajak.</p>
	<h1>Pajak Bumi dan Bangunan</h1>
	<p>
		Pajak Bumi dan Bangunan (PBB) merupakan sebuah biaya yang harus disetorkan atas keberadaan tanah dan
		bangunan yang memberikan keuntungan dan kedudukan sosial ekonomi bagi seseorang ataupun badan.
		<br><br>
		<strong>Bumi</strong> adalah permukaan bumi yang meliputi tanah dan perairan pedalaman serta laut
		wilayah kabupaten/kota.
		Sedangkan yang dimaksud bangunan adalah konstruksi teknis yang ditanam atau diletakkan secara tetap pada
		tanah dan/atau laut.
	</p>
